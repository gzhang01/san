		<h2>What is SAN?</h2>
		<p>
		The Student Activist Network (SAN) is an organization currently operating 
		in the Greater Boston Area which provides networking and consulting services to 
		student groups and organizes Student Activist Conferences (SACs). 
		</p>
	</div><div class="blank"><p></p></div>

	<div class="panel">
		<h2>Our Mission</h2>
		<p>
		Our mission is to enhance activist efforts at local universities by building 
		a comprehensive network that facilitates collaboration between groups. SAN is 
		working to push activist groups out of isolation by promoting the sharing of 
		valuable knowledge, resources, and experience, and by improving on groups' 
		access to local government. We aim to both help with existing projects and lay 
		down the foundation for larger-scale projects and social movements.
		</p>
